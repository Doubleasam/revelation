     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $this->validate ($request, [
            'name'=>'required',
            ]);

            $followupcat = new FollowupCategory;
            $followupcat ->name = $request->name;
            $followupcat ->days =$request->days;
            $followupcat->notes =$request->notes;
            $followupcat->save();

            return redirect (route('followupcategory.index'));
    }

